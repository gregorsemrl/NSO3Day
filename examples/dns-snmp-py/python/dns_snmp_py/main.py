# -*- mode: python; python-indent: 4 -*-
import ncs
from ncs.application import Service


# ------------------------
# SERVICE CALLBACK EXAMPLE
# ------------------------
class ServiceCallbacks(Service):

    # The create() callback is invoked inside NCS FASTMAP and
    # must always exist.
    @Service.create
    def cb_create(self, tctx, root, service, proplist):
        self.log.info('Service create(service=', service._path, ')')

        # Normally, this would be calculated, but here for the example
        # we will just copy the values from the service model parameters.
        # Note: Dashes "-" are converted to underscore "_" in Python.

        dns_server_ip = service.dns_server
        snmp_server_ip = service.snmp_server
        acl_number = service.acl_number
        community_str = service.community_string

        # Apply variables to the template
        vars = ncs.template.Variables()
        vars.add('DNS_SERVER', dns_server_ip)
        vars.add('SNMP_SERVER', snmp_server_ip)
        vars.add('ACL_NUMBER', acl_number)
        vars.add('COMMUNITY_STRING', community_str)

        template = ncs.template.Template(service)
        template.apply('dns-snmp-py-template', vars)

    # The pre_modification() and post_modification() callbacks are optional,
    # and are invoked outside FASTMAP. pre_modification() is invoked before
    # create, update, or delete of the service, as indicated by the enum
    # ncs_service_operation op parameter. Conversely
    # post_modification() is invoked after create, update, or delete
    # of the service. These functions can be useful e.g. for
    # allocations that should be stored and existing also when the
    # service instance is removed.

